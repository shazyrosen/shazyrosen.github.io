// Получаем контейнер для слов
const track = document.querySelector('.carouseltext-track');

// Функция для добавления слов
function populateTrack() {
    words.forEach(word => {
        const div = document.createElement('div');
        div.className = 'word';
        div.textContent = word;
        track.appendChild(div);
    });
}

// Инициализация
populateTrack();

// Постоянное движение
let offset = 0;
function scrollWords() {
    offset += 1; // Скорость прокрутки
    track.style.transform = `translateY(-${offset}px)`;

    // Когда список уходит полностью вверх, сбрасываем
    if (offset >= track.scrollHeight / 2) {
        offset = 0;
    }

    requestAnimationFrame(scrollWords);
}

scrollWords();